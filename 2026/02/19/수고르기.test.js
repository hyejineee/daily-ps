const { solution } = require("./수고르기");

test("예제 1", () => {
  expect(solution(3, 3, [1, 5, 3])).toBe(4);
});

test("M=0, 중복 없음 (자기 자신 비교 버그)", () => {
  expect(solution(3, 0, [1, 3, 5])).toBe(2);
});

test("M=0, 중복 있음", () => {
  expect(solution(4, 0, [1, 3, 3, 5])).toBe(0);
});

test("음수 포함, 음수-양수 쌍", () => {
  // [-5,-3,-1,2] 중 diff>=3: (-5,-1)=4, (-5,2)=7, (-3,2)=5, (-1,2)=3
  expect(solution(4, 3, [-5, -3, -1, 2])).toBe(3);
});

test("음수 포함, 균등 간격", () => {
  // [-10,-5,0,5] 중 diff>=5: 인접쌍 전부 5
  expect(solution(4, 5, [-10, -5, 0, 5])).toBe(5);
});

test("극단값 (int 범위 경계)", () => {
  // -10^9 와 10^9, 차이 = 2*10^9
  expect(solution(2, 2000000000, [-1000000000, 1000000000])).toBe(2000000000);
});
